package com.mycompany.capstoneproject.dto;

import java.time.LocalDate;

/**
 *
 * @author apprentice
 */
public class Blog {
    private Integer id;
    private LocalDate creationDate;
    private Category category;
    private Boolean pendingStatus = false;
    private String blogContent;
    private Boolean postStatus = false;
    private Boolean expirableStatus = false;
    private LocalDate postDate;
    private LocalDate expireDate;
    private String title;
    private String showDate;

    public String getShowDate() {
        return showDate;
    }

    public void setShowDate(String showDate) {
        this.showDate = showDate;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public LocalDate getCreationDate() {
        return creationDate;
    }

    public void setCreationDate(LocalDate creationDate) {
        this.creationDate = creationDate;
    }

    public Category getCategory() {
        return category;
    }

    public void setCategory(Category category) {
        this.category = category;
    }

    public Boolean getPendingStatus() {
        return pendingStatus;
    }

    public void setPendingStatus(Boolean pendingStatus) {
        this.pendingStatus = pendingStatus;
    }

    public String getBlogContent() {
        return blogContent;
    }

    public void setBlogContent(String blogContent) {
        this.blogContent = blogContent;
    }

    public Boolean getPostStatus() {
        return postStatus;
    }

    public void setPostStatus(Boolean postStatus) {
        this.postStatus = postStatus;
    }

    public Boolean getExpirableStatus() {
        return expirableStatus;
    }

    public void setExpirableStatus(Boolean expirableStatus) {
        this.expirableStatus = expirableStatus;
    }

    public LocalDate getPostDate() {
        return postDate;
    }

    public void setPostDate(LocalDate postDate) {
        this.postDate = postDate;
    }

    public LocalDate getExpireDate() {
        return expireDate;
    }

    public void setExpireDate(LocalDate expireDate) {
        this.expireDate = expireDate;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
    
    
    
}
